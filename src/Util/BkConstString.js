BkConstString.insertIntoStringWithParam = function(inputString,listParamString){
    var rtn = inputString;
    for (var i =0;i<listParamString.length;i++){
        var obj = listParamString[i];
        rtn = rtn.slice(0, obj.index) + obj.str + rtn.slice(obj.index);
    }
    return rtn;
};
BkConstString.insertIntoStringWithListString = function(inputString,listString) {
    var rtn = inputString;
    var count = 0;
    while (count < listString.length) {
        var ind = rtn.indexOf(BkConstString.SPEC_CHAR);
        if (ind == -1) {
            return rtn;
        }
        var cStr = listString[count];
        rtn = rtn.slice(0, ind) + cStr + rtn.slice(ind + BkConstString.SPEC_CHAR.length);
        count++;
    }
    return rtn;
};
